package com.test01;

public class Operator02 {
	public static void main(String[] args) {
		int a = 10, b = 20;
		boolean res1, res2, res3;
		
		res1 = (a == b);
		res2 = (a <= b);
		res3 = (a >= b);
		
		System.out.println("결과 값은.." + res1);
		System.out.println("결과 값은.." + res2);
		System.out.println("결과 값은.." + res3);
	}
	
}
